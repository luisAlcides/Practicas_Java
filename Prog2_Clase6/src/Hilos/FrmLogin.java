    public void run() {
        Thread ct = Thread.currentThread();
        while (ct == h1) {
            try {
                calcula();
                LblHora.setText(hora + ":" + minutos + ":" + segundos + ":" + "" + ampm);
                Thread.sleep(1000);
            } catch (InterruptedException ex) {

            }

        }
    }

    public void calcula() {
        Calendar calendario = new GregorianCalendar();
        ampm = calendario.get(Calendar.AM_PM) == Calendar.AM ? "AM" : "PM";

        if (ampm.equals("PM")) {
            int h = calendario.get(Calendar.HOUR_OF_DAY) - 12;
            hora = h > 9 ? "" + h : "0" + h;

        } else {
            hora = calendario.get(Calendar.HOUR_OF_DAY) > 9 ? "" + calendario.get(calendario.HOUR_OF_DAY) : "0"
                    + calendario.get(Calendar.HOUR_OF_DAY);
        }
        minutos = calendario.get(Calendar.MINUTE) > 9 ? "" + calendario.get(Calendar.MINUTE) : "0"
                + calendario.get(Calendar.MINUTE);

        segundos = calendario.get(Calendar.SECOND) > 9 ? "" + calendario.get(Calendar.SECOND) : "0"
                + calendario.get(Calendar.SECOND);
    }

