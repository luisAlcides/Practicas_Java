    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        // TODO add your handling code here:
        
        String usuario = jTFUsuario.getText();
        char[] clave = jPFContasena.getPassword();
        String contrasena = new String(clave);
        String tipoUsuario = jCMBCargo.getSelectedItem().toString();
        
        if (usuario.equals("admin") && contrasena.equals("admin") && tipoUsuario.equals("Administrador")) {
            JOptionPane.showMessageDialog(null, "Bienvenido al sistema", "Sistema", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            MAdministrar principal = new MAdministrar();
            principal.setVisible(true);
            
        } else {
            JOptionPane.showMessageDialog(null, "Contrasena y usuario incorrectos", "Error", JOptionPane.ERROR_MESSAGE);
            jTFUsuario.setText("");
            jPFContasena.setText("");
            //Para que el cursor este en el txtField
            jTFUsuario.requestFocus();
            jTFUsuario.setBackground(Color.yellow);
            jPFContasena.setBackground(Color.yellow);
        }
    }//GEN-LAST:event_jBAceptarActionPerformed

    
    
