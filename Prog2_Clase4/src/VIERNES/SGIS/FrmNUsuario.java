    private void jTFCorreoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFCorreoKeyTyped
        // TODO add your handling code here:
        
        char car = evt.getKeyChar();
        if((car<'0' || car >'9') && jTFCodigo.getText().contains(".")
                && (car!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Solamente Numeros", "Validar "
                    + "Numeros", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jTFCorreoKeyTyped

    public boolean vCorreo(String correo) {
        Pattern pat = null;
        Matcher mat = null;

        pat = Pattern.compile("^[\\w\\-\\_\\+]+(\\.[\\w\\-\\_]+)*@([A-Za-z0-9-]+"
                + "\\.)+[A-Za-z]{2,4}$");
        mat = pat.matcher(correo);
        if (mat.find()) {
            return true;
        } else {
            return false;
        }
    }


