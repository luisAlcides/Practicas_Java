    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        // TODO add your handling code here:
        if (jTFNombres.getText().equals("") && jTFApellidos.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese Correctamente \n"
                    + "Los campos con * son obligatorios", "Campos obligatorios", JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultTableModel modelo = (DefaultTableModel) jTbUsuarios.getModel();

            Object[] fila = new Object[5];
            fila[0] = jTFNombres.getText();
            fila[1] = jTFApellidos.getText();
            fila[2] = jTFCorreo.getText();
            fila[3] = jTADireccion.getText();
            fila[4] = jCBCargo.getSelectedItem().toString();

            modelo.addRow(fila);
            jTbUsuarios.setModel(modelo);
            JOptionPane.showMessageDialog(null, "Registrado Correctamente");

            jTFCodigo.setText("");
            jTFNombres.setText("");
            jTFApellidos.setText("");
            jTFCorreo.setText("");
            jTADireccion.setText("");

            jCBLectura.setSelected(false);
            jCBEscritura.setSelected(false);
            jCBPropietario.setSelected(false);

            jRBHombre.setSelected(false);
            jRBMujer.setSelected(false);
            jTFNombres.requestFocus();

        }

    }//GEN-LAST:event_jBAgregarActionPerformed

    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTbUsuarios.getModel();

        int a = jTbUsuarios.getSelectedRow();

        if (a < 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
        } else {
            int confirmar = JOptionPane.showConfirmDialog(null, "¿Eliminar Registro?");
            if (JOptionPane.OK_OPTION == confirmar) {
                model.removeRow(a);
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
            }
        }

    }//GEN-LAST:event_jBEliminarActionPerformed

