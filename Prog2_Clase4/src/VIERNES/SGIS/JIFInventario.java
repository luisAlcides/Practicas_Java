    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTbInventario.getModel();

        int a = jTbInventario.getSelectedRow();

        if (a < 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
        } else {
            int confirmar = JOptionPane.showConfirmDialog(null, "¿Eliminar Registro?");
            if (JOptionPane.OK_OPTION == confirmar) {
                model.removeRow(a);
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
            }
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTbInventario.getModel();

        int a = jTbInventario.getSelectedRow();

        if (a < 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
        } else {
            int confirmar = JOptionPane.showConfirmDialog(null, "¿Modificar?");
            if (JOptionPane.OK_OPTION == confirmar) {
                String menu = JOptionPane.showInputDialog("Elija la columna a modificar\n"
                        + "1. ID\n"
                        + "2. Nombre\n"
                        + "3. Tipo\n"
                        + "4. Cantidad\n"
                        + "5. Color\n"
                        + "6. Talla\n"
                        + "7. Precio\n");
                switch (menu) {
                    case "1":
                        int idColumn = 0;
                        String id = JOptionPane.showInputDialog("Nuevo ID");
                        model.setValueAt(id, a, idColumn);
                        break;
                    case "2":
                        int nombreColumn = 1;
                        String nombre = JOptionPane.showInputDialog("Nuevo Nombre");
                        model.setValueAt(nombre, a, nombreColumn);
                        break;
                    case "3":
                        int tipoColumn = 2;
                        String tipo = JOptionPane.showInputDialog("Nuevo Tipo");
                        model.setValueAt(tipo, a, tipoColumn);
                        break;
                    case "4":
                        int cantidadColumn = 3;
                        String cantidad = JOptionPane.showInputDialog("Nueva Cantidad");
                        model.setValueAt(cantidad, a, cantidadColumn);
                        break;
                    case "5":
                        int colorColumn = 4;
                        String color = JOptionPane.showInputDialog("Nuevo Color");
                        model.setValueAt(color, a, colorColumn);
                        break;
                    case "6":
                        int tallaColumn = 5;
                        String talla = JOptionPane.showInputDialog("Nueva Talla");
                        model.setValueAt(talla, a, tallaColumn);
                        break;

                    case "7":
                        int precioColumn = 6;
                        String precio = JOptionPane.showInputDialog("Nuevo precio");
                        model.setValueAt(precio, a, precioColumn);
                        break;

                    default:
                        JOptionPane.showMessageDialog(null, "No entiendo que quieres decir", "Algo no esta bien", JOptionPane.ERROR_MESSAGE);
                        break;
                }

                JOptionPane.showMessageDialog(null, "Registro Modificado");
            }
        }
    }//GEN-LAST:event_jBEditarActionPerformed


