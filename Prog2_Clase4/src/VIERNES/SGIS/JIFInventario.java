    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        // TODO add your handling code here:
        if (jTFID.getText().equals("") && jTFCantidad.getText().equals("")
                && jTFColor.getText().equals("") && jTFNombre.getText().equals("")
                && jTFPrecio.getText().equals("") && jTFTalla.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese Correctamente \n"
                    + "Los campos con * son obligatorios", "Campos obligatorios", JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultTableModel modelo = (DefaultTableModel) jTbInventario.getModel();

            Object[] fila = new Object[7];
            fila[0] = jTFCantidad.getText();
            fila[1] = jTFColor.getText();
            fila[2] = jTFID.getText();
            fila[3] = jTFNombre.getText();
            fila[4] = jTFPrecio.getText();
            fila[5] = jTFTalla.getText();
            fila[6] = jTFTipo.getText();

            modelo.addRow(fila);
            jTbInventario.setModel(modelo);
            JOptionPane.showMessageDialog(null, "Registrado Correctamente");

            jTFCantidad.setText("");
            jTFColor.setText("");
            jTFID.setText("");
            jTFNombre.setText("");
            jTFPrecio.setText("");
            jTFTalla.setText("");
            jTFTipo.setText("");
    }//GEN-LAST:event_jBAgregarActionPerformed
    }
