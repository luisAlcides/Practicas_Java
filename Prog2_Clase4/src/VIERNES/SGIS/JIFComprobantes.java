    private void jBRegistrarComprobanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegistrarComprobanteActionPerformed
        // TODO add your handling code here:
        if (jTFConcepto.getText().equals("") && jTFMonto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese Correctamente \n"
                    + "Los campos con * son obligatorios", "Campos obligatorios", JOptionPane.ERROR_MESSAGE);
        } else {

            DefaultTableModel modelo = (DefaultTableModel) jTbComprobante.getModel();

            Object[] fila = new Object[5];
            fila[0] = jCBCuenta.getSelectedItem().toString();
            fila[1] = jCBAxuiliar.getSelectedItem().toString();
            fila[2] = jTFConcepto.getText();
            
            if(debeHaber){
                fila[3] = valorRadio;
                
            }else{
                fila[4] = valorRadio;
            }
            
            modelo.addRow(fila);
            jTbComprobante.setModel(modelo);
            JOptionPane.showMessageDialog(null, "Registrado Correctamente");

            jTFConcepto.setText("");
            jTFMonto.setText("");

        }

    }//GEN-LAST:event_jBRegistrarComprobanteActionPerformed

    String valorRadio;
    boolean debeHaber = false;
    
    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        // TODO add your handling code here:
        
        if(jTFMonto.getText().equals("") && (jRBDebe.isSelected() == false)
                || jTFMonto.getText().equals("") && (jRBHaber.isSelected() == false)){
            JOptionPane.showMessageDialog(null, "Debe llenar los campos y seleccionar entre debe o haber");
        }else{
            
            if (jRBDebe.isSelected() == true) {
                valorRadio = jTFMonto.getText();
                debeHaber = true;
            } else {
                valorRadio = jTFMonto.getText();
                debeHaber = false;
            }
        }
        
        
        jTFMonto.setText("");
    }//GEN-LAST:event_jBAceptarActionPerformed


